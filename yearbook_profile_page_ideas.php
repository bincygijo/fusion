      <p>Using the Fusion Books system, create a <span class="quotes">&lsquo;</span>Profile<span class="quotes">&rsquo;</span> page in the Book Planner, add some questions and then invite the students to write their answers.</p>
      <p><b>HANDY HINTS:</b> Before inviting other people to submit their answers attempt to answer the questions yourself. Ensure no answers overlap and the questions invite a wide variety of answers. Keep the questions short to conserve space on the profiles and always ask positive questions that will bring out the best.</p>
