      <h1>Yearbook <span>Front Cover Ideas</span></h1>
      <p>A popular and effective way to create a yearbook cover is to stage a design contest. Students studying Graphic Design and Art can be invited to contribute their entries. If there are several yearbook covers that are very effective you may wish to dedicate a page in the yearbook to display these. Scanned art-work such as paintings or drawings or a high-quality photo can look very effective.</p>
      <p>A few ideas for your cover design:</p>
      <ul class="list">
        <li>Take photos of your students and get them to form the letters of a word for example <span class="quotes">&ldquo;</span>2009<span class="quotes">&rdquo;</span> or <span class="quotes">&ldquo;</span>Yearbook<span class="quotes">&rdquo;</span>. </li>
        <li>For a graduation book get the students to sign a blank bit of paper and scan the page into the computer</li>
        <li>For a Graduation Book take a close-up of your Leavers jumper</li>
        <li>A photo of your class-group forming a letter or number, for example <span class="quotes">&ldquo;</span>09<span class="quotes">&rdquo;</span></li>
        <li>Run a competition for students in the Art class, Photography or graphic design class to create a yearbook cover.</li>
      </ul>
      <p>On the back-cover of your yearbook you could include:</p>
      <ul class="list">
        <li>A poem written for the graduating class </li>
        <li>A collage of photos</li>
        <li>For a Primary School Yearbook bright coloured hand-prints can also look very effective</li>
      </ul>
