	 <br>
      <div class="highlight nospace">
	    <h1>The number of questions you choose to include in your set depends on:</h1>
      <ul class="list">
        <li>The number of profile questions on a page, for example:
			<ul>
		      <li>If you have two profiles per page, 8-14 questions works well</li>
				<li>If you have 6 profiles per page, 4-6 questions will fit</li>
			</ul>
		</li>
        <li>The number of questions depends on the type of questions you ask and the length of the answers, for example if you ask:
          <ul>
        		<li><span class="quotes">&ldquo;</span>Favourite Movie<span class="quotes">&rdquo;</span> a 1 line answer is likely
        		<li>However, if you ask <span class="quotes">&ldquo;</span>Most memorable moment?<span class="quotes">&rdquo;</span> a longer answer may be give.</li>
		  </ul>
		</li>
		<li>So- test your questions! Get your Yearbook committee to have a go at answering the questions and seeing how it fits on the page- before you get your whole year group to answer.</li>
      </ul>
	  </div>
	  <div class="highlight nospace">
	  <h1>Tips for writing your Yearbooks&rsquo; Profile Set</h1>
	  <ul class="list">
        <li>Avoid asking questions that will invite similar answers, for example asking the two questions below in one set of Profile Questions are likely to get the same answer:

        	<ul>
			  <li>What was a highlight this year?</li>
        		<li>What has been your favourite moment?<br />
			</ul>
			Instead, only use one of these questions and ask another question that invites a different answer, for example:
			<ul>
        		<li>What was a highlight this year?</li>
        		<li>I felt proud when&hellip;</li>
			</ul>
        	Ask questions that get an insight into their personality and get the answers you want:		</li>
        </li>
        <blockquote>For example, when asked: <em>Where do you hope to be in 10 years?</em> Students often respond <span class="quotes">&ldquo;</span>having a family<span class="quotes">&rdquo;</span> or <span class="quotes">&ldquo;</span>at our 10 year reunion<span class="quotes">&rdquo;</span>. If you are trying to find out what occupation or lifestyle they want, try <span class="quotes">&ldquo;</span>I hope to become a&hellip;<span class="quotes">&rdquo;</span> This will invite a diverse array of answers such as <span class="quotes">&lsquo;</span>a doctor<span class="quotes">&rsquo;</span>, <span class="quotes">&lsquo;</span>a lifelong traveler<span class="quotes">&rsquo;</span>, <span class="quotes">&lsquo;</span>aid worker<span class="quotes">&rsquo;</span> or a <span class="quotes">&lsquo;</span>film star<span class="quotes">&rsquo;</span>. </blockquote>
        <li>Keep your profile questions short and to the point: For example,
          <ul>
       		  <li>Don&rsquo;t ask: <em>One of my favourite events would have to be..</em></li>
        		<li>Ask: <em>My favourite event was..</em></li>
			</ul>
		</li>
        <li>Always ask questions that invite a positive response, for example:
			<ul>
       		  <li>Don&rsquo;t ask: <em>What was your least favourite subject?</em></li>
        		<li>Ask: <em>What was your favourite subjects</em></li>
			</ul>
        A common question used is <span class="quotes">&ldquo;</span><em>What was your most embarrassing moment?</em><span class="quotes">&rdquo;</span> While this invites a lot of comical responses, try asking <span class="quotes">&ldquo;</span>What was your most memorable moment?<span class="quotes">&rdquo;</span> as this is likely to give you a more diverse range of answers.</li>
        <p><b>How to create a Profile Page with Fusion Books</b></p>
